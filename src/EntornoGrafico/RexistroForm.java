    private void BotonRexistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRexistroActionPerformed
        //Comprobo se campos baleiros
        if(this.campoUsuario.getText().isEmpty()
            || this.campoContra.getText().isEmpty()
            || this.campoRepContra.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Algun dos Campos esta en Branco", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        //Comprobo se os contrasinais Coindicen
        
        //Se NON coinciden..
        else if(!this.campoContra.getText().equals(this.campoRepContra.getText())){
            JOptionPane.showMessageDialog(this,"Os Contrasinais Non Coinciden", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
        //Se coinciden procedemos tentar crear o usuarios
        else{
            String user=this.campoUsuario.getText();
            String pass=this.campoContra.getText();
            
            //Comprobo que o usuario NON exista xa
            
            //Se Existe..
            if(ListaXogadores.comprobarExiste(user)){
                JOptionPane.showMessageDialog(this,"O Usuario xa est√° Rexistrado", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //Se NON existe Rexistroo
            else{
                try {
                    ListaXogadores.engadir(new Xogador(user,pass));
                    JOptionPane.showMessageDialog(this,"O Usuario foi Rexistrado Correctamente", "Rexistro Correcto", JOptionPane.PLAIN_MESSAGE);
                } catch (UserException ex) {
                    JOptionPane.showMessageDialog(this,"O Usuario ou o Contrasinal non cumpren o Formato\n"+
                            ex.getMessage(), "Erro de Formato", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_BotonRexistroActionPerformed

